float silhoutte_score(std::vector<std::vector<float>>&src,std::vector<std::vector<float>>&centroids,std::vector<int>&labels)
{

   float distance_matrix[src.size()][src.size()];
   for(int i=0;i<src.size();i++)
   {
     for(int j=0;j<src.size();j++)
     {
       if(i!=j)
       distance_matrix[i][j]=boost::gil::calculate_distance(src[i],src[j]);
     }

   }
    std::vector<std::pair<float,float>>sample_sil_score(src.size(),{0.0f,0.0f});
    int points_in_cluster[centroids.size()]={0};
    float distance_to_points_in_cluster[centroids.size()];
    memset(distance_to_points_in_cluster,0,sizeof(distance_to_points_in_cluster));
    for(int i=0;i<src.size();i++)
    {
       points_in_cluster[labels[i]]++;
     }
    for(int i=0;i<src.size();i++)
    {
       //points_in_cluster[labels[i]]++;
       for(int j=0;j<src.size();j++)
       {
          if(i!=j)
          {
            if(labels[i]==labels[j])
            {
              sample_sil_score[i].first+=distance_matrix[i][j];
            }
            else
            {
              distance_to_points_in_cluster[labels[j]]+=distance_matrix[i][j];
            }
          }
       }
        sample_sil_score[i].first/=((float)(points_in_cluster[labels[i]]-1));
        float intercluster_distance=999;
        for(int j=0;j<centroids.size();j++)
        {
           if(j!=labels[i])
           {
               distance_to_points_in_cluster[j]/=(float)points_in_cluster[j];
               intercluster_distance=std::min(intercluster_distance,distance_to_points_in_cluster[j]);
               sample_sil_score[i].second=intercluster_distance;
           }
           distance_to_points_in_cluster[j]=0;
        }

    }
     float silhoutte_coefficient=0;
     float total_score=0;
     for(int i=0;i<src.size();i++)
     {
       silhoutte_coefficient=0;
        if(points_in_cluster[labels[i]]>1)
        {
          silhoutte_coefficient=sample_sil_score[i].first <sample_sil_score[i].second?(1-sample_sil_score[i].first/sample_sil_score[i].second):(1-sample_sil_score[i].second/sample_sil_score[i].first);

        }
        total_score+=silhoutte_coefficient;
     }
     return total_score/(float)src.size();

}
